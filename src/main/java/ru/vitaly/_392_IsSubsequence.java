public class _392_IsSubsequence {

    public static boolean isSubsequence(String s, String t) {
        int j = 0;
        for (int i = 0; i < t.length() && j < s.length(); i++) {
            if (t.charAt(i) == s.charAt(j)) j++;
        }
        return j == s.length();
    }
}